O.ORDERID,
O.ORDERDATE,
O.SHIPDATE,
O.SHIPMODE,
C.CUSTOMERNAME,
C.CUSTOMERID,
C.SEGMENT,
C.COUNTRY,
P.CATEGORY,
p.PRODUCTID,
P.PRODUCTNAME,
P.SUBCATEGORY,
